def decode_image_urls(encrypted_string):
    """调用Node.js解密图片URL列表（带重试）"""
    decoder_path = Path(__file__).parent / "decode_helper.js"
    if not decoder_path.exists():
        raise FileNotFoundError(
            f"找不到解密辅助脚本: {decoder_path}\n"
            f"请确保 decode_helper.js 与 download.py 在同一目录下"
        )

    last_error = None
    for attempt in range(1, DECODE_RETRIES + 1):
        try:
            result = subprocess.run(
                ["node", str(decoder_path)],
                input=encrypted_string,
                capture_output=True,
                text=True,
                timeout=DECODE_TIMEOUT,
                encoding="utf-8",
            )

            if result.returncode != 0:
                last_error = result.stderr.strip()
                raise RuntimeError(last_error)

            output = result.stdout.strip()
            if not output:
                last_error = "解密结果为空"
                raise ValueError(last_error)

            try:
                items = json.loads(output)
            except json.JSONDecodeError:
                urls = [line.strip() for line in output.split("\n") if line.strip()]
                return urls

            items.sort(key=lambda x: x.get("order", 0))
            urls = [item["url"] for item in items if "url" in item]
            if not urls:
                last_error = "解密结果中无有效URL"
                raise ValueError(last_error)
            return urls

        except Exception as e:
            last_error = str(e)
            if attempt < DECODE_RETRIES:
                _log_info(f"  解密重试 ({attempt}/{DECODE_RETRIES}): {last_error}")
                time.sleep(RETRY_DELAY * attempt)

    raise RuntimeError(f"解密失败（已重试{DECODE_RETRIES}次）: {last_error}")


