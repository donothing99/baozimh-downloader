def check_nodejs():
    """检查Node.js是否可用"""
    try:
        result = subprocess.run(["node", "--version"], capture_output=True,
                                text=True, timeout=5)
        version = result.stdout.strip()
        # 检查大版本 >= 18 (fetch API支持)
        major = int(re.search(r'v(\d+)', version).group(1))
        if major < 18:
            print(f"警告: Node.js {version}，建议 18+ 以支持内置fetch")
        return version
    except FileNotFoundError:
        return None
    except (subprocess.TimeoutExpired, AttributeError):
        return None


def parse_args():
    parser = argparse.ArgumentParser(
        description="包子漫画(baozimh.org)通用下载器",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  %(prog)s fannaoxiyouji                    下载"烦恼西游记"到当前目录
  %(prog)s fannaoxiyouji -o D:\\comics      下载到指定目录
  %(prog)s fannaoxiyouji --start 5 --end 10 只下载第5-10话
  %(prog)s fannaoxiyouji --list             只列出章节不下载

漫画slug即URL中的标识符，例如:
  https://m.baozimh.org/chapterlist/fannaoxiyouji
                                        ^^^^^^^^^^^^^^ 即为slug
        """,
    )
    parser.add_argument("slug", help="漫画slug (如 fannaoxiyouji)")
    parser.add_argument("-o", "--output", default=None,
                        help="保存目录 (默认: ./<漫画标题>)")
    parser.add_argument("--start", type=int, default=None,
                        help="起始章节号 (从1开始)")
    parser.add_argument("--end", type=int, default=None,
                        help="结束章节号 (含)")
    parser.add_argument("--list", action="store_true",
                        help="只列出章节，不下载")
    parser.add_argument("--no-skip", action="store_true",
                        help="不跳过已下载的章节 (默认跳过)")
    return parser.parse_args()


def main():
    args = parse_args()
    slug = args.slug.strip("/")

    print(f"包子漫画下载器")
    print(f"目标: {slug}")
    print("=" * 50)

    # 检查Node.js
    node_version = check_nodejs()
    if not node_version:
        print("错误: 未找到 Node.js")
        print("图片URL解密需要 Node.js 18+ (内置fetch支持)")
        print("请安装: https://nodejs.org/")
        sys.exit(1)
    print(f"Node.js: {node_version}")

    # 1. 获取漫画ID
    try:
        mid = get_manga_id(slug)
        print(f"漫画ID: {mid}")
    except Exception as e:
        print(f"获取漫画信息失败: {e}")
        sys.exit(1)

    # 2. 获取章节列表和标题
    try:
        manga_title, chapters = get_manga_info(mid)
    except Exception as e:
        print(f"获取章节列表失败: {e}")
        sys.exit(1)

    if not chapters:
        print("章节列表为空")
        sys.exit(0)

    # 3. 应用章节范围过滤
    if args.start or args.end:
        start = args.start or 1
        end = args.end or len(chapters)
        chapters = chapters[start - 1:end]
        print(f"筛选范围: 第{start}话 - 第{end}话 (共{len(chapters)}话)")

    # 4. 列表模式
    if args.list:
        print(f"\n{'序号':>4}  {'章节ID':>10}  标题")
        print("-" * 50)
        for i, ch in enumerate(chapters, 1):
            attrs = ch["attributes"]
            title = attrs.get("title", "").strip()
            order = attrs.get("order", i)
            print(f"{i:>4}  {ch['id']:>10}  {title}")
        return

    # 5. 确定输出目录
    if args.output:
        output_dir = Path(args.output)
    else:
        output_dir = Path.cwd() / sanitize_filename(manga_title)
    output_dir.mkdir(parents=True, exist_ok=True)
    print(f"保存目录: {output_dir}")

    # 6. 逐章下载
    total = len(chapters)
    success_chapters = 0
    fail_chapters = 0

    skip_existing = not args.no_skip

    for i, chapter in enumerate(chapters, 1):
        ok = download_chapter(mid, chapter, output_dir, i, total, skip_existing=skip_existing)
        if ok:
            success_chapters += 1
        else:
            fail_chapters += 1
        if i < total:
            time.sleep(CHAPTER_DELAY)

    # 7. 汇总
    print("\n" + "=" * 50)
    print(f"下载完成!")
    print(f"  漫画: {manga_title}")
    print(f"  成功: {success_chapters}/{total} 话")
    if fail_chapters > 0:
        print(f"  失败: {fail_chapters}/{total} 话 (重新运行即可续传，已下载章节自动跳过)")
    print(f"  保存位置: {output_dir}")


if __name__ == "__main__":
    main()