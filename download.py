def main():
    args = parse_args()
    slug = args.slug.strip("/")

    # 配置日志
    setup_logging(args.log)

    _log_info(f"包子漫画下载器")
    _log_info(f"目标: {slug}")
    _log_info("=" * 50)

    # 检查Node.js
    node_version = check_nodejs()
    if not node_version:
        _log_error("错误: 未找到 Node.js")
        _log_error("图片URL解密需要 Node.js 18+ (内置fetch支持)")
        _log_error("请安装: https://nodejs.org/")
        sys.exit(1)
    _log_info(f"Node.js: {node_version}")

    # 1. 获取漫画ID
    try:
        mid = get_manga_id(slug)
        _log_info(f"漫画ID: {mid}")
    except Exception as e:
        _log_error(f"获取漫画信息失败: {e}")
        sys.exit(1)

    # 2. 获取章节列表和标题
    try:
        manga_title, chapters = get_manga_info(mid)
    except Exception as e:
        _log_error(f"获取章节列表失败: {e}")
        sys.exit(1)

    if not chapters:
        _log_info("章节列表为空")
        sys.exit(0)

    # 3. 应用章节范围过滤
    if args.start or args.end:
        start = args.start or 1
        end = args.end or len(chapters)
        chapters = chapters[start - 1:end]
        _log_info(f"筛选范围: 第{start}话 - 第{end}话 (共{len(chapters)}话)")

    # 4. 列表模式
    if args.list:
        _log_info(f"\n{'序号':>4}  {'章节ID':>10}  标题")
        _log_info("-" * 50)
        for i, ch in enumerate(chapters, 1):
            attrs = ch["attributes"]
            title = attrs.get("title", "").strip()
            order = attrs.get("order", i)
            _log_info(f"{i:>4}  {ch['id']:>10}  {title}")
        return

    # 5. 确定输出目录
    if args.output:
        output_dir = Path(args.output)
    else:
        output_dir = Path.cwd() / sanitize_filename(manga_title)
    output_dir.mkdir(parents=True, exist_ok=True)
    _log_info(f"保存目录: {output_dir}")

    # 6. 逐章下载
    total = len(chapters)
    success_chapters = 0
    fail_chapters = 0
    skip_existing = not args.no_skip

    try:
        for i, chapter in enumerate(chapters, 1):
            ok = download_chapter(mid, chapter, output_dir, i, total,
                                  skip_existing=skip_existing)
            if ok:
                success_chapters += 1
            else:
                fail_chapters += 1
            if i < total:
                time.sleep(CHAPTER_DELAY)
    except KeyboardInterrupt:
        _log_info("\n\n用户中断 (Ctrl+C)")

    # 7. 汇总
    _log_info("\n" + "=" * 50)
    _log_info(f"下载完成!")
    _log_info(f"  漫画: {manga_title}")
    _log_info(f"  成功: {success_chapters}/{total} 话")
    if fail_chapters > 0:
        _log_info(f"  失败: {fail_chapters}/{total} 话 (重新运行即可续传，已下载页面自动跳过)")
    _log_info(f"  保存位置: {output_dir}")


if __name__ == "__main__":
    main()