import requests

# ==================== 站点配置 ====================
SITE_HOST = "https://m.baozimh.org"
CHAPTER_LIST_API = "https://v2.apikk.top/api/manga/get"
CHAPTER_INFO_API = "https://v2.apikk.top/api/v2/chapter/getinfo"
CDN_BASES = [
    "https://c-nd3-1.6wm.top",
    "https://c-nd2-1.6wm.top",
    "https://t-nd3-1.6wm.top",
    "https://t-nd2-1.6wm.top",
]

# 下载设置
MAX_RETRIES = 3
RETRY_DELAY = 2
REQUEST_TIMEOUT = 30
DOWNLOAD_TIMEOUT = 60
CHAPTER_DELAY = 1  # 章节间延迟(秒)

HEADERS = {
    "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 "
                  "(KHTML, like Gecko) Chrome/125.0.0.0 Safari/537.36",
    "Accept": "application/json, text/plain, */*",
    "Origin": SITE_HOST,
    "Referer": SITE_HOST + "/",
}

session = requests.Session()
session.headers.update(HEADERS)


# ==================== 网络请求 ====================

def get_manga_id(slug):
    """从章节列表页提取漫画ID"""
    url = f"{SITE_HOST}/chapterlist/{slug}"
    resp = session.get(url, timeout=REQUEST_TIMEOUT)
    resp.raise_for_status()
    match = re.search(r'data-mid="(\d+)"', resp.text)
    if not match:
        raise ValueError(f"无法从页面中提取漫画ID，请检查slug是否正确: {slug}")
    return match.group(1)


def get_manga_info(mid):
    """通过API获取漫画标题和章节列表"""
    print(f"正在获取漫画信息 (mid={mid})...")
    resp = session.get(CHAPTER_LIST_API, params={"mid": mid, "mode": "all"},
                       timeout=REQUEST_TIMEOUT)
    resp.raise_for_status()
    data = resp.json()
    if data.get("code") != 200:
        raise ValueError(f"API返回错误: {data}")
    info = data["data"]
    manga_title = info.get("title", "")
    chapters = info["chapters"]
    print(f"漫画: {manga_title}")
    print(f"共找到 {len(chapters)} 话")
    return manga_title, chapters


def get_chapter_images(mid, chapter_id):
    """获取章节的加密图片数据"""
    resp = session.get(
        CHAPTER_INFO_API,
        params={"m": mid, "c": chapter_id},
        timeout=REQUEST_TIMEOUT
    )
    resp.raise_for_status()
    data = resp.json()
    if data.get("code") != 200:
        raise ValueError(f"获取章节信息失败: {data}")
    info = data["data"]["info"]
    encrypted_images = info["images"]["images"]
    title = info.get("title", "").strip()
    return encrypted_images, title


# ==================== 解密 ====================

def decode_image_urls(encrypted_string):
    """调用Node.js解密图片URL列表"""
    decoder_path = Path(__file__).parent / "decode_helper.js"
    if not decoder_path.exists():
        raise FileNotFoundError(
            f"找不到解密辅助脚本: {decoder_path}\n"
            f"请确保 decode_helper.js 与 download.py 在同一目录下"
        )

    result = subprocess.run(
        ["node", str(decoder_path)],
        input=encrypted_string,
        capture_output=True,
        text=True,
        timeout=30,
        encoding="utf-8",
    )

    if result.returncode != 0:
        raise RuntimeError(f"解密失败: {result.stderr.strip()}")

    output = result.stdout.strip()
    if not output:
        raise ValueError("解密结果为空")

    try:
        items = json.loads(output)
    except json.JSONDecodeError:
        urls = [line.strip() for line in output.split("\n") if line.strip()]
        return urls

    items.sort(key=lambda x: x.get("order", 0))
    urls = [item["url"] for item in items if "url" in item]
    if not urls:
        raise ValueError("解密结果中无有效URL")
    return urls


# ==================== 下载 ====================

def sanitize_filename(name, max_length=80):
    """清理文件名，移除非法字符"""
    name = re.sub(r'[<>:"/\\|?*]', '_', name)
    name = name.strip(' .')
    if len(name) > max_length:
        name = name[:max_length].rstrip(' .')
    return name or "unnamed"


def guess_extension(url, content_type):
    """根据URL和Content-Type猜测文件扩展名"""
    url_lower = url.split("?")[0].lower()
    for ext in [".webp", ".jpg", ".jpeg", ".png", ".gif"]:
        if ext in url_lower:
            return ext
    ct = content_type.lower()
    for keyword, ext in [("webp", ".webp"), ("jpeg", ".jpg"), ("jpg", ".jpg"),
                          ("png", ".png"), ("gif", ".gif")]:
        if keyword in ct:
            return ext
    return ".jpg"


def download_image(url, save_path, retries=MAX_RETRIES):
    """下载单张图片，支持多CDN回退"""
    if url.startswith("/"):
        url_path = url
    elif url.startswith("http"):
        rest = url.split("://", 1)[1]
        url_path = rest.split("/", 1)[1] if "/" in rest else "/" + rest
    else:
        url_path = "/" + url

    for attempt in range(1, retries + 1):
        for cdn in CDN_BASES:
            full_url = cdn + url_path
            try:
                resp = session.get(full_url, timeout=DOWNLOAD_TIMEOUT, stream=True)
                if resp.status_code == 404:
                    continue
                resp.raise_for_status()

                content_type = resp.headers.get("Content-Type", "")
                if "image" not in content_type and "octet-stream" not in content_type:
                    continue

                ext = guess_extension(url_path, content_type)
                if not save_path.suffix or save_path.suffix.lower() == '.tmp':
                    save_path = save_path.with_suffix(ext)

                save_path.parent.mkdir(parents=True, exist_ok=True)
                with open(save_path, "wb") as f:
                    for chunk in resp.iter_content(chunk_size=8192):
                        f.write(chunk)

                file_size = save_path.stat().st_size
                if file_size < 1024:
                    save_path.unlink()
                    raise ValueError(f"文件过小 ({file_size} bytes)")

                return save_path, file_size

            except requests.exceptions.HTTPError:
                continue
            except Exception:
                if attempt < retries:
                    break
                raise

        if attempt < retries:
            print(f"  重试 ({attempt}/{retries}): 所有CDN均失败")
            time.sleep(RETRY_DELAY * attempt)

    raise RuntimeError(f"所有CDN均无法下载: {url_path}")


def download_chapter(mid, chapter, output_dir, chapter_index, total_chapters, skip_existing=True):
    """下载单个章节的全部图片"""
    chapter_id = chapter["id"]
    attrs = chapter["attributes"]
    title = attrs.get("title", f"第{attrs.get('order', chapter_index)}话")
    order = attrs.get("order", chapter_index)

    folder_name = sanitize_filename(f"{order:03d}_{title}")
    chapter_dir = output_dir / folder_name

    print(f"\n[{chapter_index}/{total_chapters}] {title} (id={chapter_id})")

    # 断点续传
    if skip_existing and chapter_dir.exists() and any(chapter_dir.iterdir()):
        existing = list(chapter_dir.iterdir())
        print(f"  已存在 {len(existing)} 个文件，跳过")
        return True

    try:
        encrypted_images, _ = get_chapter_images(mid, chapter_id)
    except Exception as e:
        print(f"  获取章节信息失败: {e}")
        return False

    try:
        image_urls = decode_image_urls(encrypted_images)
    except Exception as e:
        print(f"  解密图片URL失败: {e}")
        return False

    print(f"  共 {len(image_urls)} 页")

    success_count = 0
    fail_count = 0

    for i, url in enumerate(image_urls, 1):
        save_path = chapter_dir / f"{i:03d}"
        try:
            final_path, size = download_image(url, save_path)
            success_count += 1
            print(f"  [{i}/{len(image_urls)}] {final_path.name} ({size / 1024:.0f}KB)")
        except Exception as e:
            fail_count += 1
            print(f"  [{i}/{len(image_urls)}] 下载失败: {e}")

    print(f"  完成: {success_count} 成功, {fail_count} 失败")
    return fail_count == 0


